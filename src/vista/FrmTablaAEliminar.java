    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:
        FrmEliminarCuenta frmEliminarCuenta = new FrmEliminarCuenta();
        frmEliminarCuenta.lblIdCuenta.setText(String.valueOf(cuenta.getIdcuenta()));
        frmEliminarCuenta.lblSaldo.setText(String.valueOf(cuenta.getSaldo()));
        frmEliminarCuenta.lblNro.setText(cuenta.getNrodecuenta());
        frmEliminarCuenta.lblBanco.setText(String.valueOf(cuenta.getIdbanco().getIdbanco()));
        frmEliminarCuenta.lblUsuario.setText(String.valueOf(cuenta.getIdusuario().getIdusuario()));
        this.dispose();
        frmEliminarCuenta.setVisible(true);
    }//GEN-LAST:event_btnEliminarActionPerformed
    
