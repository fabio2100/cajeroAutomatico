    /*
    btn modificar: crea banco y asigna los valores de los textfields. 
    llama a controlBancos.modificarBanco y pasa como par치metro el banco creado
    finalmente advierte el resultado de la operaci칩n
    */
    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        // TODO add your handling code here:
        Banco banco = new Banco();
        banco.setIdbanco(Integer.valueOf(lblIdBanco.getText()));
        banco.setCodigo(txtFieldCodigo.getText());
        banco.setNombre(txtFieldNombre.getText());
        banco.setDireccion(txtFieldDireccion.getText());
        
        boolean res = ControlBancos.modificarBanco(banco);
        if (res){
            JOptionPane.showMessageDialog(this, "Banco modificado exitosamente");
            FrmBancos frmBancos = new FrmBancos();
            this.dispose();
            frmBancos.setVisible(true);
        }else{
            JOptionPane.showMessageDialog(this,"No se pudo modificar el banco");
            FrmBancos frmBancos = new FrmBancos();
            this.dispose();
            frmBancos.setVisible(true);
        }
    }//GEN-LAST:event_btnModificarActionPerformed

