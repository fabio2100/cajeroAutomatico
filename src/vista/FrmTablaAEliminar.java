    //Click en tabla: Asigna la fila seleccionada a la cuenta creada inicialmente
    private void tablaCuentasMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablaCuentasMouseClicked
        // TODO add your handling code here:
        String idCuenta = String.valueOf(tablaCuentas.getValueAt(tablaCuentas.getSelectedRow(),0));
        String nroCuenta = String.valueOf(tablaCuentas.getValueAt(tablaCuentas.getSelectedRow(),1));
        String banco = String.valueOf(tablaCuentas.getValueAt(tablaCuentas.getSelectedRow(),9));
        String saldo = String.valueOf(tablaCuentas.getValueAt(tablaCuentas.getSelectedRow(),2));
        String idUsuario = String.valueOf(tablaCuentas.getValueAt(tablaCuentas.getSelectedRow(),10));

        cuenta.setIdcuenta(Integer.valueOf(idCuenta));
        cuenta.setNrodecuenta(nroCuenta);

        Banco banco2 = new Banco();
        banco2.setIdbanco(Integer.valueOf(banco));
        cuenta.setIdbanco(banco2);

        cuenta.setSaldo(Double.valueOf(saldo));

        Usuario usuario2 = new Usuario();
        usuario2.setIdusuario(Integer.valueOf(idUsuario));
        cuenta.setIdusuario(usuario2);
    }//GEN-LAST:event_tablaCuentasMouseClicked
    
