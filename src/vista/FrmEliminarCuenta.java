    /*
    btn Eliminar: Crea nueva cuenta, asigna los valores de los labels 
    muestra el resultado de la operaci√≥n y cierra ventana y devuelve a anterior
    */
    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:

        Cuenta cuentaAEliminar = new Cuenta();

        cuentaAEliminar.setUsuariobaja(uSing.getIdusuario());
        
        Date fecha = new Date();
        java.sql.Date fechaBaja = new java.sql.Date(fecha.getTime());
        cuentaAEliminar.setFechabaja(fechaBaja);
        
        cuentaAEliminar.setIdcuenta(Integer.valueOf(lblIdCuenta.getText()));
        cuentaAEliminar.setNrodecuenta(lblNro.getText());
        cuentaAEliminar.setSaldo(Double.valueOf(lblSaldo.getText()));

        Banco banco = new Banco();
        banco.setIdbanco(0);

        Usuario usuario = new Usuario();
        usuario.setIdusuario(0);
        cuentaAEliminar.setIdusuario(usuario);

        cuentaAEliminar.setIdbanco(banco);
        cuentaAEliminar.setIdusuario(usuario);

        boolean modificar = ControlCuentas.eliminarCuenta(cuentaAEliminar);

        if (modificar){
            JOptionPane.showMessageDialog(this, "Cuenta eliminada correctamente");
        }else{
            JOptionPane.showMessageDialog(this, "No se pudo eliminar la cuenta");
        }
        
        FrmCuentas frmCuentas = new FrmCuentas();
        this.dispose();
        frmCuentas.setVisible(true);
    }//GEN-LAST:event_btnEliminarActionPerformed
    
